
public class Main {
    public static void main(String[] args) {
            //Math sınıf metodları
        // Math.ceil(x)  ==> bir büyük tam sayıya yuvarlama yapar
        // Math.cos(x)  ==> cos degerini döner
        // Math.sin(x)  ==> sin degerini döner
        // Math.floor(x)  ==> kücük tam sayıya yuvarlar
        // Math.max(x,y  ==>) büyük sayıyı verir
        // Math.min(x,y)  ==> kücük sayıyı verır
        // Math.pow(x,y)  ==> x üzeri y yapar yani üs alır
        // Math.random()  ==> 1 ile 0 arasında rastgele degerler
        // Math.round(x)  ==> yakın olan tam sayıya döner
        //Math.abs(x)    ==> mutlak deger
        // Math.sqrt(x)  ==> karekök

        System.out.println(Math.ceil(2.3));
        System.out.println(Math.sin(60));
        System.out.println(Math.floor(2.3));
        System.out.println(Math.max(1.2,1));
        System.out.println(Math.min(12.4 , 13));
        System.out.println(Math.pow(2,5));
        System.out.println(Math.random());
        System.out.println(Math.round(6.5));
        System.out.println(Math.abs(-5));
        System.out.println(Math.sqrt(16));
    }
}